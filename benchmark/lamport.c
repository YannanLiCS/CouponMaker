/*********************** FUNCTION DEFINITIONS ***********************/

unsigned long hash(unsigned long x) {
    x = ((x >> 16) ^ x) * 0x45d9f3b;
    x = ((x >> 16) ^ x) * 0x45d9f3b;
    x = (x >> 16) ^ x;
    return x;
}

void gen_private_key(long private_key[]) {
    long i;
    for (i = 0; i < KEY_SIZE; i++) {
        private_key[i] = rand() % INT_MAX + 1;
    }
}

void gen_public_key(long private_key[], long public_key[]) {
    long i;
    for (i = 0; i < KEY_SIZE; i++) {
        public_key[i] = hash(private_key[i]);
    }
}


    
// Sign signs an arbitrary length message using the given private key and
// returns signature.
//
// IMPORTANT: Do not use the same private key to sign more than one message!
// It's a one-time signature.
void sign(const BYTE msg[], long msg_len, long private_key[], long signature[]){
    BYTE hashed_msg[SHA256_BLOCK_SIZE];
    SHA256_CTX ctx;
    sha256_init(&ctx);
    // tranfer the msd to the length of SHA256_BLOCK_SIZE (signature)
    sha256_update(&ctx, msg, msg_len);
    sha256_final(&ctx, hashed_msg);
    // generate signature
    long x;
    long i, k;
    for (i = 0; i < SHA256_BLOCK_SIZE; i++) {
        for (k = 0; k < 8; k++) {
            x = 1 << k;
            x &= hashed_msg[i];
            if (x != 0) {
                signature[i * 8 + k] = private_key[2*(i*8+k)];
            } else {
                signature[i * 8 + k] = private_key[2*(i*8+k)+1];
            }
        }
    }
}



long verify(const BYTE msg[], long msg_len, long public_key[], long signature[]){
    BYTE hashed_msg[SHA256_BLOCK_SIZE];
    SHA256_CTX ctx;
    sha256_init(&ctx);
    // tranfer the msd to the length of SHA256_BLOCK_SIZE (signature)
    sha256_update(&ctx, msg, msg_len);
    sha256_final(&ctx, hashed_msg);
    // verify
    long x;
    long i, k;
    for (i = 0; i < SHA256_BLOCK_SIZE; i++) {
        for (k = 0; k < 8; k++) {
            x = 1 << k;
            x &= hashed_msg[i];
            if (x != 0) {
                if (hash(signature[i * 8 + k]) != public_key[2*(i*8+k)]) {
                    return -1;
                }
            } else {
                if (hash(signature[i * 8 + k]) != public_key[2*(i*8+k)+1]) {
                    return -1;
                }
            }
        }
    }
    return 0;
}


